package com.ui;

import java.awt.*;

public class Ex02_awt2 {

	public static void main(String[] args) {
		Frame f = new Frame();
		// setLocation(x,y) 창 위치 설정
		f.setLocation(500, 500);
		// setSize(x,y) 창 사이즈 설정
		f.setSize(500, 400);
		// setVisible(boolean) 창 띄우기(true) / 창 끄기(false)
		f.setVisible(true);
		
	}

}
